<h1>Поздний стоицизм Сенеки: поступок мысли</h1>
<h2>Л. Кришталева</h2>

<p>Эллинизм – эпоха непрерывных войн и пертурбаций. Исследователи считают, что политическая и социальная нестабильность привели к тому, что люди, занимавшиеся философией, были увлечены в большей степени проблемами счастья, добродетели, нежели отвлеченными вопросами о бытии и устройстве мира. Не случайно, и в стоицизме, и в эпикурействе, которые наряду со скептицизмом являлись самими влиятельными философскими школами этого периода, высшим блаженством, несмотря на различие учений, являются аналогичные состояния: бесчувствие (апатия) и безмятежность (атараксия). Считалось, что постоянство счастья достигается благодаря самодостаточности человека, владению своей душой, освобождению от случайностей внешнего мира. Эти сравнения ставят вопрос о своеобразии стоицизма.</p>

