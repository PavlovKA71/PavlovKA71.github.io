<h1>Философия у-держания бытия</h1>
<h2>С. Корнев</h2>

<p class=MsoBodyText>Наделить человека определенной мерой, да и вообще всяким достоинством способны лишь те вещи, которые своим ярким светом ослепляют глаза смотрящего. Спустя непродолжительное время, человек, прозрев от ослепления, вновь может взирать на источник этого яркого света. Вещь является человеку уже иной, как бы в другом свете. Идеи космоса, единства, истины и т. д. - вещи, манящие своей яркостью. Устремление к свету этих вещей наделяет наше внимание к ним особой отметкой. Эта отметка не только наделение человека достоинством исторического масштаба, но и лавровый венок от самого времени. И время, будучи судьбой этой самой истории, наделит человека той мерой достоинства, с которой он был внимателен к свету этих вещей.</p>

